    private void serviceComboPopupMenuWillBecomeInvisible(javax.swing.event.PopupMenuEvent evt) {//GEN-FIRST:event_serviceComboPopupMenuWillBecomeInvisible
        // TODO add your handling code here:
        String tmp = (String) serviceCombo.getSelectedItem();
        String sql = "select * from service where titre=?";
        try {
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, tmp);
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                String add1 = rs.getString("description");
                jTextArea1.setText(add1);
                
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(ServiceDao.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_serviceComboPopupMenuWillBecomeInvisible

