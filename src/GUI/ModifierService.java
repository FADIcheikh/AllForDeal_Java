    private void Fillcombo() {
        ServiceDao sd = new ServiceDao();
        CategorieServiceDao cpd = new CategorieServiceDao();
        ListeServices = sd.findByClient(LoginInfo.getClient().getId());
        System.out.println(LoginInfo.getClient().getId());
        liste = cpd.findAll();
        for (Service c : ListeServices) {
            serviceCombo.addItem(c.getTitre());
            
        }
        for (CategorieService c : liste) {
            cat.addItem(c.getNom());
            comp.addItem(c.getNom());
        }
    }

