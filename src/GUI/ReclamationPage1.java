    private void envoyerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_envoyerActionPerformed
        Reclamation reclamation = new Reclamation();
      if ((tf_sujet.getText().isEmpty()) || (tf_reclamation.getText().isEmpty()))
      alertRec.showMessageDialog(null,"Veilliez remplir les champs","erreur",JOptionPane.ERROR_MESSAGE);
      else {
       reclamation.setTitre(tf_sujet.getText());
       reclamation.setDescription(tf_reclamation.getText());
       
      reclamation.setEtat("Non Trait√©");
      reclamation.setId_client(LoginInfo.getClient().getId());
      
      
      DateFormat dateFormat = new SimpleDateFormat("yyyy/MM/dd");
      Date date = new Date();
      java.sql.Date sqlDate = new java.sql.Date(date.getTime());
      reclamation.setDate(sqlDate);
       
       ReclamationDao rec = new ReclamationDao();
       rec.AjouterReclamation(reclamation);
       this.dispose();
      }
    }//GEN-LAST:event_envoyerActionPerformed

