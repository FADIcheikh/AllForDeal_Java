package Entities;

import java.util.logging.Logger;

/**
 *
 * @author Maynoo
 */
public class Publicite {

    private int id;
    private String titre, path, description;

    public Publicite() {
    }

    public Publicite(int id, String titre, String path, String description) {
        this.id = id;
        this.titre = titre;
        this.path = path;
        this.description = description;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitre() {
        return titre;
    }

    public void setTitre(String titre) {
        this.titre = titre;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

}
